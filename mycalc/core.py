def add(a: float, b: float) -> float:
    """
    Add two numbers.

    Parameters
    ----------
    a : float
        First number
    b : float
        Second number

    Returns
    -------
    float
        Sum of a and b
    """
    return a + b

def sub(a: float, b: float) -> float:
    """
    Subtract two numbers.

    Parameters
    ----------
    a : float
        The number to subtract from
    b : float
        The number to subtract

    Returns
    -------
    float
        The result of a - b
    """
    return a - b
